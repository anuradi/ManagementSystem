    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         try {
            if (aroom.getSelectedItem().toString().equals("<-Select->")) {
                JOptionPane.showMessageDialog(rootPane, "Select the Room name");
            } else if (asession.getSelectedItem().toString().equals("<-Select->")) {
                JOptionPane.showMessageDialog(rootPane, "Select the Session");
            } else {
                String room = aroom.getSelectedItem().toString();
                String session = asession.getSelectedItem().toString();
                String roomid = "";
                String sessionid = "";
                ResultSet rs1 = DB.DB.search("select idbuilding from building where room_name='" + room + "'");
                if (rs1.first()) {
                    roomid = rs1.getString(1);
                }
                ResultSet rs2 = DB.DB.search("select idlecture_sessions from lecture_sessions where subject_code='" + session + "'");
                if (rs2.first()) {
                    sessionid = rs2.getString(1);
                }
                DB.DB.iud("update session_rooms set lecture_sessions='"+sessionid+"', building='"+roomid+"' where idsession_rooms='"+srid+"'");
                JOptionPane.showMessageDialog(rootPane, "Successfully Updated");
                clearFields();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

