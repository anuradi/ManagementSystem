    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            if (dtm.getValueAt(selectedRow, 3).toString().equals("Lecture Hall")) {
                lhall.setSelected(true);
            } else {
                lab.setSelected(true);
            }
            bname.setText(dtm.getValueAt(selectedRow, 1).toString());
            rname.setText(dtm.getValueAt(selectedRow, 2).toString());
            rcapacity.setText(dtm.getValueAt(selectedRow, 4).toString());
            locationid = dtm.getValueAt(selectedRow, 0).toString();
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (bname.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Building name is empty");
            } else if (rname.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Romm Name is empty");
            } else if (rcapacity.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Capacity is empty");
            } else {
                String building = bname.getText();
                 String room = rname.getText();
                 String capacity = rcapacity.getText();
                 String roomtype="";
                 if (lhall.isSelected()) {
                     roomtype="Lecture Hall";
                 }else{
                     roomtype="Laboratory";
                 }
                 DB.DB.iud("update building set name='"+building+"' , room_name='"+room+"', type='"+roomtype+"', capacity='"+capacity+"' where idbuilding='"+locationid+"' ");
                 JOptionPane.showMessageDialog(rootPane, "Succesfully updated");
                 clearFields();
                 loadTableData();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

