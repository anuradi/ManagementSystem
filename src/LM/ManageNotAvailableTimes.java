    private void refreshBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshBtnActionPerformed
        // Refresh Button
         try {
            con = (Connection) DBconnection.getConnection();
            stmt = con.createStatement();
            String sql = "SELECT not_available_time.notAvailbleTime_id,not_available_time.time, lecturer.name,student_groups.groupno,student_groups.subgroupno FROM not_available_time INNER JOIN lecturer ON not_available_time.lecturer_id = lecturer.idlecturer INNER JOIN student_groups ON not_available_time.group_id = student_groups.idstudent_groups";
            rs = stmt.executeQuery(sql);
             DefaultTableModel tableModel = (DefaultTableModel)jTable1.getModel();
             tableModel.setRowCount(0);
             
            while (rs.next()) {
              
              Object obj[] = {rs.getInt("notAvailbleTime_id"),rs.getString("time"),rs.getString("name"),rs.getInt("groupno"),rs.getString("subgroupno")};
              
              tableModel.addRow(obj);
             
            }
        } catch (SQLException e) {
            e.printStackTrace();
             JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_refreshBtnActionPerformed

