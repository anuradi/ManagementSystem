    private void loadTableData() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = DB.DB.search("SELECT sr.idsession_rooms,ls.subject_code,b.room_name from session_rooms sr JOIN lecture_sessions ls on sr.lecture_sessions=ls.idlecture_sessions join building b on sr.building=b.idbuilding where sr.status!='deactive'");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void loadRooms() {
        try {
            aroom.addItem("<-Select->");
            ResultSet rs = DB.DB.search("select distinct room_name from building");
            while (rs.next()) {
                aroom.addItem(rs.getString(1));
            }
        } catch (Exception e) {
        }
    }
    
    private void loadSessions() {
        try {
            asession.addItem("<-Select->");
            ResultSet rs = DB.DB.search("select subject_code name from lecture_sessions");
            while (rs.next()) {
                asession.addItem(rs.getString(1));
            }
        } catch (Exception e) {
        }
    }

    private void clearFields() {
        aroom.setSelectedItem("<-Select->");
        asession.setSelectedItem("<-Select->");
        loadTableData();
    }

    private void deleteSessionRoom() {
        try {
            DB.DB.iud("update session_rooms set status='deactive' where idsession_rooms='" + srid + "'");
            JOptionPane.showMessageDialog(rootPane, "Successfully Deleted");
            loadTableData();
            clearFields();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
