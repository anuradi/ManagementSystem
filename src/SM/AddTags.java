    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = tagname.getText();
        String code = tagcode.getText();
        String tags = relatedtags.getSelectedItem().toString();
        if(name.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Tag name is empty");
        }else if(code.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Tag code is empty");
        }else if(tags.equals("<-Select->")){
            JOptionPane.showMessageDialog(rootPane, "Please select a tag");
        }else{
            try {
                DB.DB.iud("insert into tags (name, code, related_tag) values ('"+name+"','"+code+"','"+tags+"')");
                JOptionPane.showMessageDialog(rootPane, "Successfully inserted");
                clearFields();
            } catch (Exception ex) {
                Logger.getLogger(AddTags.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

