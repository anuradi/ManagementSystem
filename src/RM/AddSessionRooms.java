    private void loadRooms() {
        try {
            aroom.addItem("<-Select->");
            ResultSet rs = DB.DB.search("select distinct room_name from building");
            while (rs.next()) {
                aroom.addItem(rs.getString(1));
            }
        } catch (Exception e) {
        }
    }

    private void loadSessions() {
        try {
            asession.addItem("<-Select->");
            ResultSet rs = DB.DB.search("select subject_code name from lecture_sessions");
            while (rs.next()) {
                asession.addItem(rs.getString(1));
            }
        } catch (Exception e) {
        }
    }

    private void clearFields() {
        aroom.setSelectedItem("<-Select->");
        asession.setSelectedItem("<-Select->");
    }
